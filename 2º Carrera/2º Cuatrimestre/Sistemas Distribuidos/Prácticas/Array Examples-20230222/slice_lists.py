# Python Program to Slice Lists

my_list = [1, 2, 3, 4, 5]

# Get all the items
print(my_list[:])

# Get all the items after a specific position
print(my_list[2:])

# Get all the items before a specific position
print(my_list[:2])

# Get all the items from one position to another position
print(my_list[2:4])

# Get the items at specified intervals (in this case from two to two)
print(my_list[::2])